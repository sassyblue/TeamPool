namespace TeamPool.Entities
{
    public class Grid
    {
        private int width;
        private int height;
        private int colWidth;
        private int colHeight;
        
        public Grid(int width, int height, int colWidth, int colHeight)
        {
            this.width = width;
            this.height = height;
            this.colWidth = colWidth;
            this.colHeight = colHeight;
        }
        
        
    }
}